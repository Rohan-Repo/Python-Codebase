"""
IDEA - WRITING IN ALL CAPS IS LIKE YELLING. Best to use your “indoor voice” sometimes, writing entirely in lowercase.
Implement a program in Python that prompts the user for input and then outputs that same input in uppercase and lowercase.
Punctuation and whitespace should be outputted unchanged.
"""

inp_str = input('Enter the Text:')
print( 'Entered Text: ', inp_str )
print( 'Upper-Case: ', inp_str.upper() )
print( 'Lower-Case: ', inp_str.lower() )